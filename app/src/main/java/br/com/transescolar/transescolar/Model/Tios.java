package br.com.transescolar.transescolar.Model;

public class Tios {

    private int id;
    private String nomeT;
    private String email;
    private String cpfT;
    private String apelido;
    private String placa;
    private String imgT;
    private String tellT;
    private String senhaT;
    private String locali;


    public int getUid() {
        return id;
    }

    public void setUid(int uid) {
        this.id = uid;
    }

    public String getNomeT() {
        return nomeT;
    }

    public void setNomeT(String nomeT) {
        this.nomeT = nomeT;
    }

    public String getCpfT() {
        return cpfT;
    }

    public void setCpfT(String cpfT) {
        this.cpfT = cpfT;
    }

    public String getApelido() {
        return apelido;
    }

    public void setApelido(String apelido) {
        this.apelido = apelido;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getImgT() {
        return imgT;
    }

    public void setImgT(String imgT) {
        this.imgT = imgT;
    }

    public String getTellT() {
        return tellT;
    }

    public void setTellT(String tellT) {
        this.tellT = tellT;
    }

    public String getSenhaT() {
        return senhaT;
    }

    public void setSenhaT(String senhaT) {
        this.senhaT = senhaT;
    }

    public String getLocali() {
        return locali;
    }

    public void setLocali(String locali) {
        this.locali = locali;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
